package ruiz;

public class ExplicacionPatronesDeDiseño {

    /**
     *
     * Factory: Permite gracias a una interfaz crear objetos dentro de una superclase permitiendo que las subclases
     * cambien el tipo de objetos que van a crear. Permite ahorrar recursos del sistema mediante la reutilización
     * de objetos existentes en lugar de tener que volver a construirlos.
     *
     * Builder: Se utiliza para crear diferentes objetos siguiendo una serie de pasos reutilizando código,
     * simplifica la necesidad de crear subclases para cada configuración posible de un objeto en particular.
     *
     * Prototype: Permite crear nuevos objetos a partir de objetos existentes, con una declaración de interfa (clonar)
     * este patrón encarga a los objetos la acción de clonarse.
     *
     * Singleton: Permite restringir la creación de nuevas instancias de una clase a un objeto determinado. Podría
     * aplicarse cuando una clase solo deba tener una instancia disponible para todos los usuarios de la aplicación.
     *
     * Adapter: Permite que objetos con interfaces incompatibles colaboren entre sí. Permitiendo utilizar programas
     * antiguos a los que se le van actualizando las funciones.
     *
     * Decorator: Permite extender el comportamiento de un objeto añadiendo funcionalidades al mismo a través de
     * objetos encapsuladores que presentan dichas funcionalidades.
     *
     * Facade: Permite bajo una fachada simplificada que esconde un subsistema complejo. Permite integrar una aplicación
     * con una biblioteca que tiene muchas funciones de las que solo se ocupan unas pocas.
     *
     */
}
